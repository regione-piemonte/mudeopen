package it.csi.mudeopen.mudeopensrvapi.vo.geo.polygon;
public class CSICoordinate {
	
    protected Double x = Double.NaN;
    protected Double y = Double.NaN;
    protected Double z = Double.NaN;
	public CSICoordinate(Double x, Double y) {
		this.x = x;
		this.y = y;
	}

	public Double getX() {
		return x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public Double getY() {
		return y;
	}

	public void setY(Double y) {
		this.y = y;
	}

	public Double getZ() {
		return z;
	}

	public void setZ(Double z) {
		this.z = z;
	}
}
